							<h3>Persian-Arabic Glossary of Sri Guru Granth Sahib</h3>
							<p>There are more than one thousand Persian words that have been employed by the contributors of Sri Guru Granth Sahib. Without getting into the proper meanings of these words, it is very difficult to understand their occurrence and connotation in Gurbani. With the help of Dr. Amarwant Singh (formerly Professor and Head of Persian Department, Pbi. University, Patiala) the centre has worked on the Persian-Arabic Glossary of Sri Guru Granth Sahib. It contains about three thousand entries wherein besides the root meanings in Persian, their occurrence in Gurbani along with their context and examples have been explained. This project is complete and it will be released at the time of inauguration of the building of the Centre.</p>
