							<h3>Value System of Sri Guru Granth Sahib</h3>
							<p>The Sikh Scripture has propounded a new kind of World view which is all inclusive and holistic in nature. It stands for balance and harmony between the temporal and the spiritual known as Miri-Piri system.  The authors of Sikh Scripture had envisioned a society free from all kinds of evil where human beings may experience the Bliss of God in this very life. It required total transformation of human personality wedded to the values of highest kind. This project undertaken by Dr. Jaswinder Kaur Dhillon, Visiting Professor deals with the Value System of Sri Guru Granth Sahib from axiological perspective. To make the Hindi knowing world the ethical precepts of Sikh Scripture this work has been especially produced in Hindi. It is now under publication and will be released very shortly.</p>
