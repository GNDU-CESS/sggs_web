						<div class="" style="padding:40px;">
							<h3>Recording of Oral Sikh Tradition and Partition of Punjab Related Stories</h3>
							<p>In order to spread the Mission of Sikhism Guru Nanak and his successors had travelled a lot in and outside the Punjab. The oral Sikh tradition that is in circulation at these places has not found its way into the written sources. To supplement as well as to corroborate the evidence, oral Sikh tradition is of great value, thus it needs recording at the earliest. Similarly, in 1947 in the wake of partition of Punjab millions of Sikhs/Punjabis had to leave their homes and hearths. Persons who have witnessed the partition and those who actually experienced it are vanishing day by day. Thus, the heart rendering stories that are enshrined in their bosoms will be lost forever. The Centre has taken upon itself to save it from oblivion. This project was initiated in collaboration with American India Foundation and 1947 Partition Archives, University of California, Berkeley. Besides appointing a Fellow the AIF provided equipment for the purpose of recording. An exhaustive questionnaire has been prepared to conduct the interview on Camera. In the current session Centre plans to publish some of the most significant interviews in a book form..</p>
