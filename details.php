            Dr.Balwant Singh Dhillon (b. Feb.5, 1950) currently Adjunct Professor, has been the Founder Director (2011-Feb.2015), Centre for Studies on Sri Guru Granth Sahib, Guru Nanak Dev University, Amritsar; Professor and Head, Department of Guru Nanak Studies; Dean of Faculty of Humanities and Religious Studies. He has also served on the Senate and Syndicate of Guru Nanak Dev University, Amritsar. He did his Master in History from University of Rajasthan, Jaipur (1974); and in Religious Studies from Punjabi University, Patiala (1976) and was awarded Ph. D. by Guru Nanak Dev University, Amritsar in1985 for his thesis on The Sikh Gurus and the Mughal State.Dr. Dhillon specializes in the study and analysis of documents/manuscripts in Rajasthani, Hindi, Gurmukhi and Persian. He is a keen scholar of Sikh Studies, especially history, religion and literature. So far he has published/edited a dozen of books and contributed more than hundred papers published in research journals, books and encyclopedias. Among his earlier works Parmukh Sikh te Sikh Panth presents an exhaustive account of the expansion of Sikh Panth during the early 17th century and Early Sikh Scriptural Tradition: Myth and Reality responds to the issues that have cropped up from the textual studies of the Sikh scripture. He has also produced critical text of Sri Gur Panth Parkash (by Rattan Singh Bhangu), the only Sikh source on eighteenth century Sikh history. His latest work on the Eighteenth Century Persian Historiography on Banda Singh Bahadur brings into light a number of contemporary and eye witness accounts which have remained ignored by the scholars. He has been the Editor of two research journals- Journal of Sikh Studies, and Perspectives on Guru Granth Sahib both published by Guru Nanak Dev University, Amritsar. He has organized a number of National and International seminars/conferences at Guru Nanak Dev University, Amritsar. His participation in seminars/conferences at National and International level has been very consistent and fruitful. His contribution to the field of Sikh Studies, especially the study of Sikh scripture, history and religion, the Sikh institutions and their role in the formation of Sikh identity, analysis of contemporary sources of the Sikh tradition and several other debatable issues in Sikh history and religion is commendable. Presently, some important research projects are in progress under his supervision. Besides he is working on two projects--Rajasthani Sources of the Sikh History and Persian Sources on the Sikh Gurus. He may be contacted via email: balwantdhillon@yahoo.com and contacted at (0091)9855057624.
            
